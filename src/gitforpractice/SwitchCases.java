package gitforpractice;

public class SwitchCases {
	
	
	public static void main(String[] args) {
		
		
		
		String str = "test";
		
		switch (str) {
		case "test":
			System.out.println("yes");
		break;
		case "test1":
			System.out.println("NO");
		break;
		default:
			System.out.println("default");
		}

	}

}
